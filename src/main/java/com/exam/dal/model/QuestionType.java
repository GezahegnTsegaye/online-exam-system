package com.exam.dal.model;

public enum QuestionType {
    MULTIPLE_CHOICE,     // Single correct answer from multiple options
    MULTIPLE_RESPONSE,   // Multiple correct answers
    TRUE_FALSE,          // Binary (True/False) question
    SHORT_ANSWER,        // Requires text input
    ESSAY,               // Long-form answer
    MATCHING,            // Match items from two columns
    FILL_IN_BLANK,        // Fill in missing words or phrases
    SINGLE_CHOICE

}
